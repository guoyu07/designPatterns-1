package com.yc.pattern.command;

/**
 * @author Yue Chang
 * @ClassName: LightOffCommand
 * @Description: 关闭电灯命令类
 * @date 2017/7/9 22:50
 */
public class LightOffCommand implements Command {

    private Light light;

    public LightOffCommand(Light light) {
        this.light = light;
    }

    @Override
    public void execute() {
        light.off();
    }

    @Override
    public void undo() {
        light.on();
    }
}

